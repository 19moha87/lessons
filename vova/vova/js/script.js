const searchBody = document.querySelector('.search__body');
document.addEventListener('click', search);
//const button = document.querySelector('.search__button');

function search(e) {
   if (e.target.closest('.search__button')) {
      searchBody.classList.toggle('active');
   }
   else if (!e.target.closest('.search__body')) {
      searchBody.classList.remove('active');
   }
};

document.addEventListener('keyup', function(e){
   if(e.code==='Escape'){
      searchBody.classList.remove('active');
   }
});



const txtItem = document.querySelector(".search__item");
const txtItemlimit = txtItem.getAttribute('maxlength');
const txtCounter = document.querySelector('.search__counter span');
txtCounter.innerHTML = txtItemlimit;

txtItem.addEventListener('keyup', txtSetCounter);
txtItem.addEventListener('keydown', function (e) {
   if (e.repeat) txtSetCounter();
});

function txtSetCounter() {
   const txtCounterResult = txtItemlimit - txtItem.value.length;
   txtCounter.innerHTML = txtCounterResult;
}

